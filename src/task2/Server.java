    public static String connectRemote(ArrayList data) throws RemoteException{
        
        String replyServer="";
        
        try {
            Registry reg = LocateRegistry.getRegistry("", 4444);
            Functions fun = (Functions) reg.lookup("h1_server");
            
            replyServer = fun.send(data);
            
            if(replyServer.equals("Primeiro registro!")){
                register(data);
            }
    
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(null, "Erro1: "+ e.getMessage());
        } catch (NotBoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro2: "+ ex.getMessage());
        }
        
        return replyServer;
        
    }
    
    public static int check(String password){
        
        int test=0;
        
        ArrayList<User> users = new ArrayList<>();
        UserDAO udao = new UserDAO();
        users = udao.retornaTodos();
        
        for(int i=0; i<users.size(); i++){
            if(users.get(i).getPassword().equals(password)){
                test=1;
            }
        }        
        
        return test;
        
    }
    
    public static void register(ArrayList data){
        
        String imc2 = (String) data.get(0);
        double imc = Double.parseDouble(imc2);
        String ip = (String) data.get(1);
        String password = (String) data.get(2);
        
        lbIpClient.setText(ip);
        
        if(check(password) == 0){
            User u = new User(imc, ip, password);
            UserDAO udao = new UserDAO();
            udao.adiciona(u);            
        } else{
            System.out.println("JÃ¡ cadastrado!");
        }
            
        
    }
    
